public class main {
    public static void main(String[] args) {
        System.out.println(sum(2,9));
        System.out.println(average(5,3));
    }

    public static int average(int bil1, int bil2){
        return (bil1 + bil2) / 2;
    }
    public static int sum(int bil1, int bil2) {
        return bil1 + bil2;
    }
}


